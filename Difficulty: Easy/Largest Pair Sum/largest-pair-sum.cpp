class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int mx = INT_MIN;
        int smx = INT_MIN;
        
        for(auto i: arr){
            if(i>mx){
                smx=mx;
                mx = i;
            } else if(i>smx && i<mx){
                smx = i;
            }
        }
        return mx+smx;
    }
};


