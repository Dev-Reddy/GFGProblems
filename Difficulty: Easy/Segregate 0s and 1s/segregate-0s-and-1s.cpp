class Solution {
  public:
    void segregate0and1(vector<int> &arr) {
        // code here
        int i=0,j,n=arr.size();
        while(arr[i]==0) i++;    //move i to first occurence of one
        j=i;  //start j from i
        while(j<n){
            if(arr[j]==0) {swap(arr[i],arr[j]);i++;}  //swap 0 with first occurence of 1 and move i forward
            j++; //move j forward
        }
    }
};

